public enum CardType {
    // 0: Ace of Clubs
    ACECLUBS,
    // 1: King of Clubs
    KINGCLUBS,
    // 2: Queen of Clubs
    QUEENCLUBS,
    // 3: Ace of Diamonds
    ACEDIAMONDS,
    // 4: King of Diamonds
    KINGDIAMONDS,
    // 5: Queen of Diamonds
    QUEENDIAMONDS,
    // 6: Unassigned
    UNASSIGNED
}
